Notes:
	- Run this Query after creating the table SupplierContactInfo
	- This Query needs the following tables to run properly:
		- Contract_Data
		- SupplierAssignmentContactInfo
		- SapPartsList
*/

Insert [References].[dbo].SupplierContactInfo ([Pbg],[Mfg],[PrcStgy],[CC],[Grp],[VendorNbr])
Select Distinct [Pbg],[Mfg],[PrcStgy],[CC],[Grp],[VendorNbr] from [CentralDbs].[dbo].SapPartsList

