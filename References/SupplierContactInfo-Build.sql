-- Insert SupplName
-- ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

UPDATE
	[References].[dbo].[SupplierContactInfo]
SET
	SupplierContactInfo.SupplName = T2.[Record Name]
FROM
	[References].[dbo].[SupplierContactInfo] T1
INNER JOIN
	[References].[dbo].[contract_data] T2
ON
	T1.Mfg = T2.Sup_cd

